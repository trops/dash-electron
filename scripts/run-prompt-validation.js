#!/usr/bin/env node
/**
 * run-prompt-validation — spawn the actual Claude CLI for each of the
 * 10 approved prompt-validation scenarios and capture the AI's
 * first-response widget code, one-shot, no iteration.
 *
 * Mirrors how the live Widget Builder modal invokes Claude:
 *   - Same argv as dash-core's `buildClaudeCliArgs` (cliController.js
 *     ~line 177): `-p --disable-slash-commands --permission-mode
 *     bypassPermissions --output-format stream-json --verbose
 *     --system-prompt <prompt>`.
 *   - cwd = the dash-electron project root so the
 *     `.claude/skills/dash-widget-builder/` skill auto-loads (same
 *     skill the modal relies on).
 *   - User text piped via stdin.
 *
 * What the runner does NOT do:
 *   - Send a follow-up message. One-shot. The fixtures are the AI's
 *     very first turn — if it asks a clarifying question instead of
 *     emitting code (per buildSystemPrompt's first-response style),
 *     that's what we save.
 *   - Retry on bad output. If parsing fails, the raw transcript is
 *     still on disk for the user to inspect.
 *   - Run AcceptanceScorecard. Scoring lives in the sibling Jest
 *     suite (`promptValidation.test.js`) that reads the persisted
 *     fixtures — the runner is a Node-CommonJS process and can't
 *     `require()` the scorecard (which imports React). Splitting
 *     generation from scoring keeps both pieces simple.
 *
 * Output layout:
 *   test/fixtures/widgets/@ai-built/prompt-validation/
 *     raw/<scenarioId>.md         <- full assistant text (every turn)
 *     widgets/<Name>.js           <- extracted component (when parseable)
 *     widgets/<Name>.dash.js      <- extracted config   (when parseable)
 *     manifest.json               <- {id, prompt, parseStatus, files[]}
 *     dash.json                   <- package manifest (one-time)
 *
 * Usage:
 *   node scripts/run-prompt-validation.js              # run all 10
 *   node scripts/run-prompt-validation.js 01-slack     # run one by id-prefix
 *   node scripts/run-prompt-validation.js --skip-existing
 */

const fs = require("fs");
const path = require("path");
const { spawn } = require("child_process");

const { SCENARIOS } = require("../src/AiAssistant/promptValidation/scenarios");
const { buildSystemPrompt } = require("../src/AiAssistant/widgetBuilderPrompt");

const PROJECT_ROOT = path.resolve(__dirname, "..");
const FIXTURE_ROOT = path.join(
    PROJECT_ROOT,
    "test/fixtures/widgets/@ai-built/prompt-validation"
);
const RAW_DIR = path.join(FIXTURE_ROOT, "raw");
const WIDGETS_DIR = path.join(FIXTURE_ROOT, "widgets");
const MANIFEST_PATH = path.join(FIXTURE_ROOT, "manifest.json");
const PACKAGE_MANIFEST_PATH = path.join(FIXTURE_ROOT, "dash.json");

const PER_SCENARIO_TIMEOUT_MS = 5 * 60 * 1000;

function parseArgs(argv) {
    const opts = { filter: null, skipExisting: false };
    for (const arg of argv.slice(2)) {
        if (arg === "--skip-existing") opts.skipExisting = true;
        else if (!arg.startsWith("--")) opts.filter = arg;
    }
    return opts;
}

function ensureDirs() {
    fs.mkdirSync(RAW_DIR, { recursive: true });
    fs.mkdirSync(WIDGETS_DIR, { recursive: true });
    if (!fs.existsSync(PACKAGE_MANIFEST_PATH)) {
        fs.writeFileSync(
            PACKAGE_MANIFEST_PATH,
            JSON.stringify(
                {
                    name: "@ai-built/prompt-validation",
                    description:
                        "Fixtures generated by scripts/run-prompt-validation.js. First-response AI output for the 10 approved scenarios. Not for publish.",
                    version: "0.0.0",
                },
                null,
                4
            ) + "\n"
        );
    }
}

function resolveClaudeBinary() {
    // Trust PATH. Same approach as the user's terminal. The modal's
    // resolveCliBinary walks several install locations to find
    // claude.cmd on Windows + claude on macOS/Linux. For this script we
    // assume macOS/Linux (user is on darwin) and rely on shell PATH.
    return "claude";
}

function runOneScenario(scenario) {
    return new Promise((resolve) => {
        // skipFirstResponseStyle: the modal's prompt normally tells
        // the AI to open with a brief clarifying question before
        // emitting code. That's the modal's chat UX; it's not what
        // we're measuring here. We want "user types prompt -> AI
        // returns widget code, stop." So we omit that section for
        // the validation runner and keep everything else identical.
        const systemPrompt = buildSystemPrompt({
            selectedProvider: scenario.selectedProvider,
            installedProviders: scenario.installedProviders,
            skipFirstResponseStyle: true,
        });

        const args = [
            "-p",
            "--disable-slash-commands",
            "--permission-mode",
            "bypassPermissions",
            "--output-format",
            "stream-json",
            "--verbose",
            "--system-prompt",
            systemPrompt,
        ];

        const startedAt = Date.now();
        const child = spawn(resolveClaudeBinary(), args, {
            cwd: PROJECT_ROOT,
            env: process.env,
            stdio: ["pipe", "pipe", "pipe"],
        });

        let stdoutBuffer = "";
        let stderrBuffer = "";
        let assistantText = "";
        let resultMeta = null;

        child.stdin.write(scenario.userPrompt);
        child.stdin.end();

        const timeout = setTimeout(() => {
            child.kill("SIGKILL");
        }, PER_SCENARIO_TIMEOUT_MS);

        child.stdout.on("data", (chunk) => {
            stdoutBuffer += chunk.toString();
            const lines = stdoutBuffer.split("\n");
            stdoutBuffer = lines.pop();
            for (const line of lines) {
                if (!line.trim()) continue;
                let parsed;
                try {
                    parsed = JSON.parse(line);
                } catch {
                    continue;
                }
                if (parsed.type === "assistant" && parsed.message?.content) {
                    for (const block of parsed.message.content) {
                        if (block?.type === "text" && block.text) {
                            assistantText += block.text;
                        }
                    }
                } else if (parsed.type === "result") {
                    resultMeta = parsed;
                }
            }
        });

        child.stderr.on("data", (chunk) => {
            stderrBuffer += chunk.toString();
        });

        child.on("close", (code) => {
            clearTimeout(timeout);
            if (stdoutBuffer.trim()) {
                try {
                    const parsed = JSON.parse(stdoutBuffer);
                    if (
                        parsed.type === "assistant" &&
                        parsed.message?.content
                    ) {
                        for (const block of parsed.message.content) {
                            if (block?.type === "text" && block.text) {
                                assistantText += block.text;
                            }
                        }
                    }
                } catch {
                    // ignore
                }
            }
            resolve({
                exitCode: code,
                durationMs: Date.now() - startedAt,
                assistantText,
                stderrBuffer,
                resultMeta,
            });
        });
    });
}

function extractCodeBlocks(text) {
    if (!text) return [];
    const regex =
        /```(jsx|javascript|js|react|tsx|typescript)?\s*\n([\s\S]*?)```/g;
    const out = [];
    let match;
    while ((match = regex.exec(text)) !== null) {
        out.push({
            language: (match[1] || "").toLowerCase() || null,
            content: match[2].trim(),
        });
    }
    return out;
}

function extractWidgetName(componentCode) {
    if (!componentCode) return null;
    const patterns = [
        /export\s+default\s+function\s+(\w+)/,
        /export\s+(?:const|let|var)\s+(\w+)\s*=/,
        /export\s+function\s+(\w+)/,
        /function\s+(\w+)\s*\([^)]*\)\s*\{[\s\S]*export\s+default\s+\1/,
    ];
    for (const re of patterns) {
        const m = componentCode.match(re);
        if (m) return m[1];
    }
    return null;
}

function classifyBlocks(blocks) {
    let componentCode = null;
    let configCode = null;
    for (const b of blocks) {
        if (
            !componentCode &&
            (b.language === "jsx" ||
                b.language === "tsx" ||
                b.language === "react" ||
                /export\s+default\s+function\s+\w+/.test(b.content) ||
                /import\s+React/.test(b.content))
        ) {
            componentCode = b.content;
            continue;
        }
        if (
            !configCode &&
            (b.content.includes("workspace:") ||
                b.content.includes("canHaveChildren") ||
                /providers:\s*\[/.test(b.content))
        ) {
            configCode = b.content;
            continue;
        }
    }
    if (!componentCode && !configCode && blocks.length >= 2) {
        componentCode = blocks[0].content;
        configCode = blocks[1].content;
    } else if (!componentCode && blocks.length === 1) {
        componentCode = blocks[0].content;
    }
    return { componentCode, configCode };
}

function scenarioFallbackName(scenario) {
    return scenario.id
        .replace(/^\d+-/, "")
        .split("-")
        .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
        .join("");
}

function safeWriteWidgetFiles(scenario, componentCode, configCode) {
    const name =
        extractWidgetName(componentCode) || scenarioFallbackName(scenario);
    const componentPath = path.join(WIDGETS_DIR, `${name}.js`);
    const configPath = path.join(WIDGETS_DIR, `${name}.dash.js`);
    const files = [];
    if (componentCode) {
        fs.writeFileSync(
            componentPath,
            componentCode.replace(/\s+$/, "") + "\n"
        );
        files.push(path.relative(FIXTURE_ROOT, componentPath));
    }
    if (configCode) {
        fs.writeFileSync(configPath, configCode.replace(/\s+$/, "") + "\n");
        files.push(path.relative(FIXTURE_ROOT, configPath));
    }
    return { name, files };
}

async function main() {
    const opts = parseArgs(process.argv);
    ensureDirs();

    const filtered = opts.filter
        ? SCENARIOS.filter((s) => s.id.startsWith(opts.filter))
        : SCENARIOS;
    if (filtered.length === 0) {
        console.error(`No scenarios matched filter '${opts.filter}'.`);
        process.exit(1);
    }

    const manifest = fs.existsSync(MANIFEST_PATH)
        ? JSON.parse(fs.readFileSync(MANIFEST_PATH, "utf8"))
        : { runs: [] };
    const runMap = new Map(manifest.runs.map((r) => [r.id, r]));

    console.log(
        `Running ${filtered.length} scenario(s) with cwd=${PROJECT_ROOT}`
    );

    for (const scenario of filtered) {
        const rawPath = path.join(RAW_DIR, `${scenario.id}.md`);
        if (opts.skipExisting && fs.existsSync(rawPath)) {
            console.log(`  [skip] ${scenario.id} (already generated)`);
            continue;
        }

        process.stdout.write(`  [run]  ${scenario.id} ... `);
        const t0 = Date.now();
        const { exitCode, assistantText, stderrBuffer, resultMeta } =
            await runOneScenario(scenario);
        const durationMs = Date.now() - t0;

        const rawHeader =
            `<!-- scenario: ${scenario.id} -->\n` +
            `<!-- userPrompt: ${scenario.userPrompt.replace(
                /-->/g,
                ""
            )} -->\n` +
            `<!-- exitCode: ${exitCode} -->\n` +
            `<!-- durationMs: ${durationMs} -->\n` +
            (resultMeta?.is_error ? `<!-- is_error: true -->\n` : "") +
            (stderrBuffer
                ? `<!--\nstderr:\n${stderrBuffer.replace(/-->/g, "")}\n-->\n`
                : "") +
            "\n";
        fs.writeFileSync(rawPath, rawHeader + (assistantText || ""));

        const blocks = extractCodeBlocks(assistantText);
        const { componentCode, configCode } = classifyBlocks(blocks);
        let parseStatus = "no-code";
        let writtenFiles = [];
        if (componentCode || configCode) {
            const { files } = safeWriteWidgetFiles(
                scenario,
                componentCode,
                configCode
            );
            writtenFiles = files;
            parseStatus =
                componentCode && configCode
                    ? "both"
                    : componentCode
                    ? "component-only"
                    : "config-only";
        }

        runMap.set(scenario.id, {
            id: scenario.id,
            userPrompt: scenario.userPrompt,
            selectedProvider: scenario.selectedProvider,
            generatedAt: new Date().toISOString(),
            durationMs,
            exitCode,
            parseStatus,
            blockCount: blocks.length,
            assistantTextLength: assistantText.length,
            files: writtenFiles,
        });

        console.log(
            `${parseStatus} (${blocks.length} blocks, ${(
                durationMs / 1000
            ).toFixed(1)}s, exit ${exitCode})`
        );

        const runs = Array.from(runMap.values()).sort((a, b) =>
            a.id.localeCompare(b.id)
        );
        fs.writeFileSync(
            MANIFEST_PATH,
            JSON.stringify({ runs }, null, 4) + "\n"
        );
    }

    console.log("\nDone. Inspect:");
    console.log(`  ${path.relative(PROJECT_ROOT, MANIFEST_PATH)}`);
    console.log(`  ${path.relative(PROJECT_ROOT, RAW_DIR)}/`);
    console.log(`  ${path.relative(PROJECT_ROOT, WIDGETS_DIR)}/`);
    console.log("\nThen score the fixtures:");
    console.log("  npx jest src/AiAssistant/promptValidation/");
}

main().catch((err) => {
    console.error("Fatal:", err);
    process.exit(1);
});
